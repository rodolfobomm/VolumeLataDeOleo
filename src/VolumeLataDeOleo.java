public class VolumeLataDeOleo {
    public static void main(String[] args) {
        double raio = 5.0; // Atribuindo valor
        double altura = 10.0; // Atribuindo valor

        double volume = 3.14159 * raio * raio * altura; // Cálculo

        System.out.println("O volume da lata de óleo é: " + volume); // Exibindo o resultado na tela
    }
}
